package rmiserver;

import java.io.Serializable;


public abstract class User implements Serializable{
    
    int ID;
    String name;
    int age;
    int password;
    String email;

    public User(int ID, String name, int age, int password, String email) {
        this.ID = ID;
        this.name = name;
        this.age = age;
        this.password = password;
        this.email = email;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getPassword() {
        return password;
    }

    public void setPassword(int password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
    
}
