package rmiserver;

import java.io.Serializable;
import java.rmi.Remote;


public class StoreDTO implements Remote, Serializable{
   int ID; 
String name; 
String  type;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }



    
}
